// Helper to get size classes
const getSizeClass = (size?: string) => {
  const sizes = {
    xs: 'w-3 h-3',
    sm: 'w-4 h-4',
    md: 'w-5 h-5',
    lg: 'w-6 h-6',
    xl: 'w-8 h-8',
  };
  return sizes[size as keyof typeof sizes] || sizes.md;
};

// Helper to get color classes
const getColorClass = (color?: string) => {
  const colors = {
    current: 'text-current',
    neutral: 'text-neutral-900',
    gray: 'text-gray-600',
    primary: 'text-primary-500',
    success: 'text-success-500',
    danger: 'text-danger-500',
    warning: 'text-warning-500',
    premium: 'text-premium-600',
    info: 'text-info-500',
    'workflow-ship': 'text-workflow-ship',
    'workflow-preview': 'text-workflow-preview',
    'workflow-develop': 'text-workflow-develop',
  };
  return colors[color as keyof typeof colors] || colors.current;
};

